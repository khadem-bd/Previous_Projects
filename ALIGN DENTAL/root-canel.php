                    <h1>What Is the Procedure for Root Canal Therapy?</h1>
                    <p>
                      Root canal therapy normally requires you to visit your
                      dentist around three times. During the first visit, the
                      dentist will administer local anesthesia and then make a
                      small hole on the tooth's surface and into the inner
                      chamber. They will extract the decayed tissue,
                      meticulously clean and disinfect the inner chamber, and
                      reshape the small canals. The doctor then packs the
                      chamber with medicated material to prevent infection. The
                      doctor may also temporarily fill the drilled hole until a
                      permanent seal is made with a crown. A permanent
                      restoration is often needed after the root canal. If a
                      crown is required, an impression of the tooth will be made
                      after the removal of the decayed tissue, and the crown is
                      fabricated based on that impression. When the crown is
                      done, it gets bonded into place.
                    </p>
