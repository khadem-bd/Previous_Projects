                    <div class="sub-section">
                      <div class="sub-title">
                        <h3>Severe tooth decay</h3>
                      </div>
                      <p>
                        One of the chief causes of tooth decay is the plaque
                        made up of bacteria that develop on the teeth after
                        eating or drinking. If left untreated, plaque can lead
                        to the formation of holes in the teeth. These holes are
                        called cavities. Severe cavities could cause the decay
                        and deterioration of a tooth. When this occurs, the
                        dentist will have to remove the tooth.
                      </p>
                    </div>
                    <div class="sub-section">
                      <div class="sub-title">
                        <h3>Broken or cracked teeth</h3>
                      </div>
                      <p>
                        Though your teeth are quite strong, they aren’t
                        unbreakable. A tooth can sometimes crack or break when
                        you bite into something hard. Minor damage to the tooth
                        can be treated by covering the tooth with a crown. But
                        in extreme cases, the tooth may have to be extracted.
                      </p>
                    </div>
                    <div class="sub-section">
                      <div class="sub-title">
                        <h3>Impacted wisdom teeth</h3>
                      </div>
                      <p>
                        Wisdom teeth are an additional set of molars that
                        typically start to grow in the young adult years. Most
                        of us do not have enough space in our mouths for these
                        extra teeth. This often leads to teeth becoming
                        impacted, getting stuck under the gums, and causing
                        damage to already existing teeth. Impacted teeth may
                        also harm the jawbone. In these circumstances, an
                        extraction becomes unavoidable.
                      </p>
                    </div>
                    <div class="sub-section">
                      <div class="sub-title">
                        <h3>Advanced gum disease</h3>
                      </div>
                      <p>
                        Besides attacking your teeth, plaque also destroys the
                        soft gum tissue around your teeth. This could lead to
                        periodontal disease, also referred to as gum disease.
                        Gum disease causes the separation of the tissues around
                        the teeth. This leads to the loosening of teeth and can
                        also be quite painful. In severe cases, the dentist may
                        advise pulling out some of the loose teeth.
                      </p>
                    </div>

                    <div class="sub-section">
                      <div class="sub-title">
                        <h3>Crowded teeth</h3>
                      </div>
                      <p>
                        Once the permanent teeth have started to erupt, many
                        patients realize that there isn’t enough space for all
                        of their teeth. This causes misalignment of the teeth
                        and could lead to many serious dental issues in the
                        future. Patients facing this issue are usually referred
                        to an orthodontist for braces. And before these
                        treatments begin, the dentist may carry out tooth
                        extractions.
                      </p>
