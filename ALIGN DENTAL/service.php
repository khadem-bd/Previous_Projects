                  <h1>What Is Tooth Extraction?</h1>
                  <p>
                    Tooth extraction is a dental procedure where the dentist
                    extracts the tooth from the ligaments that connect it to the
                    jawbone. These ligaments are called periodontal ligaments.
                    While getting a tooth pulled out sounds a little scary, it
                    is a routinely performed procedure that will result in a
                    healthier mouth.
                  </p>
                  <a
